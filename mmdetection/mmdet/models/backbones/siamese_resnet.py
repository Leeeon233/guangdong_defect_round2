    def forward(self, _x):
        x = _x[:, 0, :]
        x2 = _x[:, 1, :]
        # if self.with_stn:
        #     x2 = self.stn(x, x2)
        # x = self.stn(x_t, x)
        # else:
        #     x2 = x_t
        x = self.conv1(x)
        x = self.norm1(x)
        x = self.relu(x)
        x = self.maxpool(x)


        x2 = self.conv1(x2)
        x2 = self.norm1(x2)
        x2 = self.relu(x2)
        x2 = self.maxpool(x2)

        if self.with_blur:
            x = self.dc(x)
            x2 = self.dc(x2)

        outs = []
        for i, layer_name in enumerate(self.res_layers):
            res_layer = getattr(self, layer_name)
            x = res_layer(x)
            x2 = res_layer(x2)
            if i in self.out_indices:
                outs.append(x - x2)
        return tuple(outs)

    def train(self, mode=True):
        super(SiameseResNet, self).train(mode)
        self._freeze_stages()
        if mode and self.norm_eval:
            for m in self.modules():
                # trick: eval have effect on BatchNorm only
                if isinstance(m, _BatchNorm):
                    m.eval()
